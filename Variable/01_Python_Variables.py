# =====================================================
# PYTHON VARIABLES - BASIC CONCEPT
# =====================================================

# =========================
# 1. Creating Variables
# =========================

# Variable dibuat saat pertama kali diberi nilai
x = 5
y = "John"

print(x)
print(y)


# =========================
# 2. Dynamic Typing (tipe data bisa berubah)
# =========================

x = 4       # x bertipe int
x = "Sally" # x sekarang bertipe str

print(x)


# =========================
# 3. Casting (Menentukan tipe data)
# =========================

a = str(3)     # "3"
b = int(3)     # 3
c = float(3)   # 3.0

print(a)
print(type(a))
print(b)
print(type(b))
print(c)
print(type(c))